    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        if(referenceText.getText().length() < 1 ||
            textArea.getText().length() < 1){
            JOptionPane.showMessageDialog(this, "Please input text and reference!",
                "Error",JOptionPane.ERROR_MESSAGE);
        }
        int dialogResult = JOptionPane.showConfirmDialog (null,
            "Are you sure you want to save changes?", "Warning",
            JOptionPane.YES_NO_OPTION);
        if(dialogResult == JOptionPane.YES_OPTION){
            curWords.setText(textArea.getText());
            curWords.setVersion(versionText.getText());
            
            //If the word entry is new, add it to the library  
            if(newWords){
                String reference = referenceText.getText();
                reference = reference.replace(":", " ");
                String indices[] = reference.split(" ");
                for(String s : indices){
                    curWords.addIndex(s);
                }
                home.addText(curWords);
            }
        }
        home.editClose(this);
    }//GEN-LAST:event_saveButtonActionPerformed

