    private void loadData(){
        String indicesText = String.format("%s %s:%s", curWords.getIndices().get(0),
            curWords.getIndices().get(1), curWords.getIndices().get(2));
        referenceText.setText(indicesText);
        //If the verse already has data, we don't want to change the reference
        referenceText.setEditable(false);
        versionText.setText(curWords.getVersion());
        textArea.setText(curWords.getText());
        firstReviewLabel.setText(String.format("First review date:     %tD", 
                curWords.getStartDate()));
    }
    
