    private void dateResetButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dateResetButtonActionPerformed
        int dialogResult = JOptionPane.showConfirmDialog (null,
            "Are you sure you want to reset reviews?", "Warning",
            JOptionPane.YES_NO_OPTION);
        if(dialogResult == JOptionPane.YES_OPTION){
            curWords.setStartDate(null);
            curWords.setLastDate(null);
            curWords.setNextDate(null);
            curWords.setDateIncrement(1);
            loadData();
            saveButton.setEnabled(true);
        }
    }//GEN-LAST:event_dateResetButtonActionPerformed

